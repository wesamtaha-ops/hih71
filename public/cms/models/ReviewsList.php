    public function advancedSearchWhere($default = false)
    {
        global $Security;
        $where = "";
        if (!$Security->canSearch()) {
            return "";
        }
        $this->buildSearchSql($where, $this->id, $default, false); // id
        $this->buildSearchSql($where, $this->from_user_id, $default, false); // from_user_id
        $this->buildSearchSql($where, $this->to_user_id, $default, false); // to_user_id
        $this->buildSearchSql($where, $this->points, $default, false); // points
