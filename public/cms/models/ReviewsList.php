        if ($tmpBool != "1" && $tmpBool != "0") {
            $tmpBool = !empty($tmpBool) ? "1" : "0";
        }
        $this->approved->setDbValueDef($rsnew, $tmpBool, 0, strval($this->approved->CurrentValue) == "");

